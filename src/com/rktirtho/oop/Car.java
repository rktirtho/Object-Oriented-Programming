package com.rktirtho.oop;

 class Car {
	String model;
	String brand;
	String color;
	double price;
	float fuel;
	int door;
	int engine;
	int year;
	
	public Car(String model, String brand, String color, double price,
			float fule, int door, int engine, int year) {
		
		this.model = model;
		this.brand = brand;
		this.color = color;
		this.price = price;
		fuel = fule;
		this.door = door;
		this.engine = engine;
		this.year = year;
		
		
	}
	

}
 
 